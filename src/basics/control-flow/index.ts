let age: number = 21;

if (age >= 21) {
  console.log('You can drink');
} else if (age >= 18) {
  console.log('You can vote');
} else {
  console.log('You are too young');
}

const isAwake: boolean = true;

if (isAwake) {
  console.log('Get up');
} else if (isAwake === false) {
  console.log('Sleeping');
} else {
  console.log('Deep sleep');
}
